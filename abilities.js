let currentAbility = {};
let abilityScore = 0;


document.addEventListener('DOMContentLoaded', () => {
    const abilityImage = document.getElementById('ability-image');
    const abilityInput = document.getElementById('ability-input');
    const abilityResult = document.getElementById('ability-result');
    const abilitySubmitBtn = document.getElementById('ability-submit-btn');

    abilitySubmitBtn.addEventListener('click', () => {
        const userInput = abilityInput.value.trim().toLowerCase();
        if (userInput === currentAbility.name.toLowerCase()) {
            abilityScore++;
            showAbilityResult(true);
        } else {
            showAbilityResult(false);
        }
    });

    function loadRandomAbility() {
        currentAbility = championsAbilities[Math.floor(Math.random() * championsAbilities.length)];
        abilityImage.src = currentAbility.abilities[Math.floor(Math.random() * currentAbility.abilities.length)];
        abilityInput.value = '';
        abilityResult.textContent = '';
    }

    function showAbilityResult(correct) {
        const iconContainer = document.createElement('div');
        iconContainer.classList.add('result-icon');

        const icon = document.createElement('img');
        icon.setAttribute('alt', 'Result Icon');

        if (correct) {
            icon.setAttribute('src', './Images/Icons/checkmark.svg');
            abilityResult.textContent = `Richtig! Deine Punktzahl: ${abilityScore}`;
            abilityResult.style.color = 'green';
        } else {
            icon.setAttribute('src', './Images/Icons/x-mark.svg');
            abilityResult.textContent = `Falsch! Es war: ${currentAbility.name}`;
            abilityResult.style.color = 'red';
        }

        iconContainer.appendChild(icon);
        abilityResult.appendChild(iconContainer);

        
        setTimeout(() => {
            iconContainer.remove();
            loadRandomAbility();
        }, 2000);
    }

    
    function goToMainMenu() {
        
        const mainMenu = document.getElementById('main-menu') || parent.document.getElementById('main-menu');
        if (mainMenu) {
            mainMenu.style.display = 'block';
    
            
            const quizContainer = document.getElementById('quiz-container');
            if (quizContainer) {
                quizContainer.style.display = 'none';
            }
    
            
            const abilityContainer = document.getElementById('ability-container');
            if (abilityContainer) {
                abilityContainer.style.display = 'none';
            }
        }
    }

    
    const logo = document.getElementById('logo');
    if (logo) {
        logo.addEventListener('click', goToMainMenu);
    }

    loadRandomAbility();
});

